#include "Delay.h"
/****************************************************************************
* 名称：DelayNS()
* 功能：长软件延时
* 入口参数：dly		延时参数，值越大，延时越久
* 出口参数：无
* 说明    ：约1.13ms
****************************************************************************/
void  DelayNS(uint32  dly)
{  
	uint32  i;

   	for(; dly>0; dly--) 
      	for(i=0; i<50000; i++);
}
/****************************************************************************
* 名称：delayms();
* 功能：短软件延时
* 入口参数：无
* 出口参数：无
* 说明    ：约70us
****************************************************************************/
void  delayms(void)
{  
	uint16  i,j;

   	for(i=0;i<400;i++)
		for(j=0;j<10;j++);
}